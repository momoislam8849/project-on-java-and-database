    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(dob.getText().trim().isEmpty()|id.getText().trim().isEmpty()|food.trim().isEmpty()|travel.trim().isEmpty()){
            k.setText("FILL EACH FIELD");
           
        }
        else{
        double total=5000.00;
        if(yes.isSelected())
        {
            total=total+1500.00;
        }
        else if(no.isSelected())
        {
            total=total;
        }
        if(bus.isSelected())
        {
            total=total+2000.00;
        }
        if(train.isSelected())
        {
            total=total+1600.00;
        }
        if(flight.isSelected())
        {
            total=total+5000.00;
        }
        showtotal.setText(Double.toString(total));
        total=Double.parseDouble(showtotal.getText());
        try
        {
            conn=mysql_connect.connectDB();
            Statement stt = conn.createStatement();
            PreparedStatement update_std = conn.prepareStatement("INSERT INTO rangamati (id,food,travel,date,showtotal) VALUES (?,?,?,?,?)");
            update_std.setString(1,id.getText());
            update_std.setString(2,food);
            update_std.setString(3,travel);
            update_std.setString(4,dob.getText());
            update_std.setString(5,showtotal.getText());
   
            
            update_std.executeUpdate();
            conn.close();
            
            
        }
        catch(Exception e)
        {
            System.out.print("Do not connect to DB - ERROR:"+e);
        }
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

